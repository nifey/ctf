data = [0x56,0x9b,0x5f,0x83,0xde,0xbe,0x45,0x65,0xe9,0xb1,0x51,0xcf,0x7f,0xd5,0x71,0x35,0xaa,0x5e,0x19,0xda,0xd9,0x9e,0x19,0xf9,0x48,0xda,0x43,0xdb]

mul = 128
mod = 257

for i in range(0,len(data)):
    for j in range(0,257):
        if (j * mul) % mod == data[i]:
            print(chr(j), end="")
            if j % 2 == 1:
                mul -= 1
            else:
                mul += 1
